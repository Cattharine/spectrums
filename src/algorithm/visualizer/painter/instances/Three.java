package algorithm.visualizer.painter.instances;

public class Three {
    private double x;
    private double y;
    private int num;

    public Three(double x, double y, int num) {
        this.x = x;
        this.y = y;
        this.num = num;
    }

    public double getY() {
        return y;
    }

    public double getX() {
        return x;
    }

    public int getNum() {
        return num;
    }
}
